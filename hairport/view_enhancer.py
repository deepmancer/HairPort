def flush_gpu_memory():
    """Clear GPU memory cache."""
    gc.collect()
    torch.cuda.empty_cache()


def apply_white_background(image: Image.Image, mask: Image.Image = None) -> Image.Image:
    """
    Apply white background to an image.
    
    Args:
        image: The image (RGB or RGBA). If RGBA, uses embedded alpha. If RGB, uses mask.
        mask: Optional alpha mask (grayscale). Used only if image is RGB.
        
    Returns:
        RGB image with white background
    """
    # Get or create RGBA image with proper alpha
    if image.mode == "RGBA":
        # Use the embedded alpha channel directly
        rgba_image = image.copy()
    else:
        # Convert to RGBA (will have full opacity by default)
        rgba_image = image.convert("RGBA")
        # Apply mask as alpha if provided
        if mask is not None:
            mask_l = mask.convert("L") if mask.mode != "L" else mask
            if mask_l.size != rgba_image.size:
                mask_l = mask_l.resize(rgba_image.size, Image.Resampling.LANCZOS)
            rgba_image.putalpha(mask_l)
    
    # Create white background
    white_bg = Image.new("RGBA", rgba_image.size, (*_bg_color(), 255))
    
    # Composite image onto white background
    result = Image.alpha_composite(white_bg, rgba_image)
    
    # Convert to RGB to ensure no alpha channel in output
    final = result.convert("RGB")
    
    return final


def preprocess_image_with_matting(
    image: Union[str, Path, Image.Image],
    bg_remover: BackgroundRemover,
    target_size: Tuple[int, int] = (1024, 1024),
) -> Image.Image:
    """
    Apply matting to an image and convert background to white.
    
    Args:
        image: Input image (path or PIL Image)
        bg_remover: BackgroundRemover instance
        target_size: Target size (width, height)
        
    Returns:
        Processed image with white background
    """
    # Load image if path
    if isinstance(image, (str, Path)):
        image = Image.open(image).convert("RGB")
    elif image.mode != "RGB":
        image = image.convert("RGB")
    
    # Remove background (returns RGBA image and mask)
    foreground, mask = bg_remover.remove_background(image)
    
    # Apply white background
    result = apply_white_background(foreground, mask)
    
    # Resize to target size if needed
    if result.size != target_size:
        result = result.resize(target_size, Image.Resampling.LANCZOS)
    
    result = result.convert("RGB")
    return result


def compute_output_dimensions(
    image: Image.Image,
    max_size: int | None = None,
) -> Tuple[int, int]:
    """
    Compute output dimensions maintaining aspect ratio with both sides as multiples of 8.
    
    Args:
        image: Input image
        max_size: Maximum dimension size
        
    Returns:
        Tuple of (width, height)
    """
    if max_size is None:
        max_size = get_config().enhance_view.max_image_size
    img_width, img_height = image.size
    aspect_ratio = img_width / img_height
    
    if aspect_ratio >= 1:  # Landscape or square
        new_width = max_size
        new_height = int(max_size / aspect_ratio)
    else:  # Portrait
        new_height = max_size
        new_width = int(max_size * aspect_ratio)
    
    # Round to nearest multiple of 8
    new_width = round(new_width / 8) * 8
    new_height = round(new_height / 8) * 8
    
    # Ensure within valid range
    new_width = max(256, min(max_size, new_width))
    new_height = max(256, min(max_size, new_height))
    
    return new_width, new_height


def get_foreground_bbox(mask: Image.Image) -> Tuple[int, int, int, int]:
    """
    Get the bounding box of the foreground from a mask.
    
    Args:
        mask: Grayscale mask where foreground is white (255)
        
    Returns:
        Tuple of (left, top, right, bottom) bounding box
    """
    # Ensure mask is in L mode
    if mask.mode != "L":
        mask = mask.convert("L")
    
    mask_array = np.array(mask)
    
    # Find non-zero (foreground) pixels
    rows = np.any(mask_array > 127, axis=1)
    cols = np.any(mask_array > 127, axis=0)
    
    if not np.any(rows) or not np.any(cols):
        # No foreground found, return full image
        return (0, 0, mask.width, mask.height)
    
    top, bottom = np.where(rows)[0][[0, -1]]
    left, right = np.where(cols)[0][[0, -1]]
    
    return (int(left), int(top), int(right) + 1, int(bottom) + 1)


def crop_and_center_foreground(
    image: Image.Image,
    mask: Image.Image,
    padding_ratio: float = 0.05,
    target_size: Tuple[int, int] = (1024, 1024),
) -> Tuple[Image.Image, Dict]:
    """
    Crop the foreground from an image, center it with padding, and resize to target size.
    
    The foreground will occupy (1 - 2*padding_ratio) of the output canvas in the 
    constraining dimension, ensuring consistent padding regardless of foreground size.
    
    Args:
        image: Input RGB image
        mask: Foreground mask (white = foreground)
        padding_ratio: Padding ratio around the foreground in OUTPUT space (0.1 = 10% on each side)
        target_size: Target output size (width, height)
        
    Returns:
        Tuple of (cropped_centered_image, crop_info_dict)
        crop_info_dict contains information needed to uncrop later
    """
    # Ensure image is RGB
    if image.mode != "RGB":
        image = image.convert("RGB")
    
    # Get foreground bounding box
    bbox = get_foreground_bbox(mask)
    left, top, right, bottom = bbox
    
    fg_width = right - left
    fg_height = bottom - top
    
    img_width, img_height = image.size
    target_width, target_height = target_size
    
    # Calculate scale so that foreground occupies (1 - 2*padding_ratio) of target
    # e.g., with padding_ratio=0.1, foreground should be 80% of target in constraining dimension
    desired_fg_ratio = 1.0 - 2.0 * padding_ratio
    scale_x = (target_width * desired_fg_ratio) / fg_width
    scale_y = (target_height * desired_fg_ratio) / fg_height
    scale = min(scale_x, scale_y)
    
    # Calculate the size of foreground after scaling
    scaled_fg_width = int(fg_width * scale)
    scaled_fg_height = int(fg_height * scale)
    
    # Calculate paste position to center foreground in target canvas
    paste_x = (target_width - scaled_fg_width) // 2
    paste_y = (target_height - scaled_fg_height) // 2
    
    # Crop just the foreground region from source image
    cropped_fg = image.crop((left, top, right, bottom))
    
    # Resize foreground to target scale
    resized_fg = cropped_fg.resize((scaled_fg_width, scaled_fg_height), Image.Resampling.LANCZOS)
    
    # Create white background canvas and paste centered foreground
    canvas = Image.new("RGB", target_size, _bg_color())
    canvas.paste(resized_fg, (paste_x, paste_y))
    
    # Store crop info for later uncropping
    crop_info = {
        "original_size": (img_width, img_height),
        "foreground_bbox": (left, top, right, bottom),  # Original foreground location
        "scale": scale,
        "paste_offset": (paste_x, paste_y),
        "scaled_fg_size": (scaled_fg_width, scaled_fg_height),
        "target_size": target_size,
    }
    
    return canvas, crop_info


def uncrop_to_original(
    enhanced_image: Image.Image,
    crop_info: Dict,
) -> Image.Image:
    """
    Uncrop the enhanced image back to the original image space.
    
    This function maps the entire enhanced canvas (including padding areas where
    the enhancement may have extended content) back to original image space.
    
    Args:
        enhanced_image: The enhanced image (target_size)
        crop_info: Dictionary with crop information from crop_and_center_foreground
        
    Returns:
        Image placed back in original spatial dimensions with white background
    """
    paste_x, paste_y = crop_info["paste_offset"]
    scale = crop_info["scale"]
    left, top, right, bottom = crop_info["foreground_bbox"]
    original_width, original_height = crop_info["original_size"]
    target_width, target_height = crop_info["target_size"]
    
    # Calculate what region in original space the entire canvas represents
    # Canvas point (0,0) maps to original point (left - paste_x/scale, top - paste_y/scale)
    canvas_origin_in_orig_x = left - paste_x / scale
    canvas_origin_in_orig_y = top - paste_y / scale
    
    # The full canvas size in original space
    canvas_size_in_orig_x = target_width / scale
    canvas_size_in_orig_y = target_height / scale
    
    # Resize entire enhanced canvas to original scale
    orig_scale_width = int(round(canvas_size_in_orig_x))
    orig_scale_height = int(round(canvas_size_in_orig_y))
    
    resized_canvas = enhanced_image.resize(
        (orig_scale_width, orig_scale_height), 
        Image.Resampling.LANCZOS
    )
    
    # Create output image with white background
    output = Image.new("RGB", (original_width, original_height), _bg_color())
    
    # Calculate paste position in original space (may be negative or extend beyond bounds)
    paste_orig_x = int(round(canvas_origin_in_orig_x))
    paste_orig_y = int(round(canvas_origin_in_orig_y))
    
    # Handle boundary clipping - determine which part of resized_canvas to copy
    # and where to paste it in the output
    src_left = max(0, -paste_orig_x)
    src_top = max(0, -paste_orig_y)
    src_right = min(orig_scale_width, original_width - paste_orig_x)
    src_bottom = min(orig_scale_height, original_height - paste_orig_y)
    
    dst_left = max(0, paste_orig_x)
    dst_top = max(0, paste_orig_y)
    
    # Extract and paste the valid region
    if src_right > src_left and src_bottom > src_top:
        region = resized_canvas.crop((src_left, src_top, src_right, src_bottom))
        output.paste(region, (dst_left, dst_top))
    
    return output


