class ViewEnhancer:
    """
    Enhancer for images generated by view_generator.py using FLUX.2 Klein 9B.
    """
    
    def __init__(
        self,
        model_id: str | None = None,
        device: str | None = None,
        dtype: torch.dtype = torch.bfloat16,
        load_pipeline: bool = True,
    ):
        """
        Initialize the ViewEnhancer.
        
        Args:
            model_id: HuggingFace model repository ID
            device: Device to run the model on
            dtype: Data type for model weights
            load_pipeline: Whether to load the pipeline immediately
        """
        cfg = get_config()
        self.model_id = model_id or cfg.models.flux_klein
        self.device = device or cfg.device
        self.dtype = dtype
        self.pipe = None
        self.bg_remover = None
        
        if load_pipeline:
            self.load_pipeline()
    
    def load_pipeline(self):
        """Load the FLUX.2 Klein pipeline and background remover."""
        self.pipe = Flux2KleinPipeline.from_pretrained(
            self.model_id,
            torch_dtype=self.dtype,
        )
        self.pipe.to(self.device)
        
        # Initialize background remover
        self.bg_remover = BackgroundRemover(device=self.device)
    
    def enhance_image_phase1(
        self,
        generated_image: Union[str, Path, Image.Image],
        prompt: Optional[str] = None,
        num_inference_steps: int | None = None,
        guidance_scale: float | None = None,
        seed: int = -1,
        width: Optional[int] = None,
        height: Optional[int] = None,
    ) -> Image.Image:
        """
        Phase 1: Enhance a generated image to fix artifacts and improve realism.
        
        Note: This method expects the image to already be centered/preprocessed.
        The centering is done in enhance_image() before calling this method.
        
        Args:
            generated_image: The preprocessed/centered image
            prompt: Custom prompt (uses FIRST_PHASE_PROMPT if None)
            num_inference_steps: Number of inference steps (default: 4 for distilled)
            guidance_scale: Guidance scale (default: 1.0 for distilled)
            seed: Random seed (-1 for random)
            width: Output width (auto-computed if None)
            height: Output height (auto-computed if None)
            
        Returns:
            Enhanced image (Phase 1 output)
        """
        if self.pipe is None:
            self.load_pipeline()

        ev = get_config().enhance_view
        if num_inference_steps is None:
            num_inference_steps = ev.num_inference_steps
        if guidance_scale is None:
            guidance_scale = ev.guidance_scale
        
        # Load image if path
        if isinstance(generated_image, (str, Path)):
            generated_image = Image.open(generated_image).convert("RGB")
        
        # Compute dimensions if not provided
        if width is None or height is None:
            width, height = compute_output_dimensions(generated_image)
        
        target_size = (width, height)
        
        # Apply matting to the image (convert background to white)
        generated_matted = preprocess_image_with_matting(
            generated_image,
            self.bg_remover,
            target_size=target_size,
        )
        
        # Single image input for Phase 1
        image_list = [generated_matted]
        
        # Use Phase 1 prompt if not provided
        if prompt is None:
            prompt = get_config().prompts.enhance_first_phase
        
        # Set up generator for reproducibility
        if seed != -1 and isinstance(seed, int):
            generator = torch.Generator(device=self.device).manual_seed(seed)
        else:
            seed = random.randint(0, np.iinfo(np.int32).max)
            generator = torch.Generator(device=self.device).manual_seed(seed)
        
        # Run the pipeline
        result = self.pipe(
            prompt=prompt,
            image=image_list,
            height=height,
            width=width,
            num_inference_steps=num_inference_steps,
            guidance_scale=guidance_scale,
            generator=generator,
        )
        
        enhanced_image = result.images[0]
        
        # Ensure output is RGB with white background
        if enhanced_image.mode != "RGB":
            enhanced_image = enhanced_image.convert("RGB")
        
        # Apply matting to ensure white background
        enhanced_image = preprocess_image_with_matting(
            enhanced_image,
            self.bg_remover,
            target_size=target_size,
        )
        
        return enhanced_image
    
    def enhance_image_phase2(
        self,
        phase1_image: Union[str, Path, Image.Image],
        reference_image: Union[str, Path, Image.Image],
        prompt: Optional[str] = None,
        num_inference_steps: int | None = None,
        guidance_scale: float | None = None,
        seed: int = -1,
        width: Optional[int] = None,
        height: Optional[int] = None,
    ) -> Image.Image:
        """
        Phase 2: Enhance the Phase 1 output using reference image for identity matching.
        
        Args:
            phase1_image: The output from Phase 1
            reference_image: The original reference image for identity
            prompt: Custom prompt (uses SECOND_PHASE_PROMPT if None)
            num_inference_steps: Number of inference steps (default: 4 for distilled)
            guidance_scale: Guidance scale (default: 1.0 for distilled)
            seed: Random seed (-1 for random)
            width: Output width (auto-computed if None)
            height: Output height (auto-computed if None)
            
        Returns:
            Enhanced image (Phase 2 / final output)
        """
        if self.pipe is None:
            self.load_pipeline()

        ev = get_config().enhance_view
        if num_inference_steps is None:
            num_inference_steps = ev.num_inference_steps
        if guidance_scale is None:
            guidance_scale = ev.guidance_scale

        # Load images if paths
        if isinstance(phase1_image, (str, Path)):
            phase1_image = Image.open(phase1_image).convert("RGB")
        if isinstance(reference_image, (str, Path)):
            reference_image = Image.open(reference_image).convert("RGB")
        
        # Compute dimensions if not provided
        if width is None or height is None:
            width, height = compute_output_dimensions(phase1_image)
        
        # Apply matting to both images (convert background to white)
        phase1_matted = preprocess_image_with_matting(
            phase1_image,
            self.bg_remover,
            target_size=(width, height),
        )
        
        reference_matted = preprocess_image_with_matting(
            reference_image,
            self.bg_remover,
            target_size=(width, height),
        )
        
        # Prepare image list for FLUX.2 Klein (Phase 1 output + reference)
        image_list = [phase1_matted, reference_matted]
        
        # Use Phase 2 prompt if not provided
        if prompt is None:
            prompt = get_config().prompts.enhance_second_phase
        
        # Set up generator for reproducibility
        if seed != -1 and isinstance(seed, int):
            generator = torch.Generator(device=self.device).manual_seed(seed)
        else:
            seed = random.randint(0, np.iinfo(np.int32).max)
            generator = torch.Generator(device=self.device).manual_seed(seed)
        
        # Run the pipeline
        result = self.pipe(
            prompt=prompt,
            image=image_list,
            height=height,
            width=width,
            num_inference_steps=num_inference_steps,
            guidance_scale=guidance_scale,
            generator=generator,
        )
        
        enhanced_image = result.images[0]
        
        return enhanced_image
    
    def enhance_image(
        self,
        generated_image: Union[str, Path, Image.Image],
        reference_image: Union[str, Path, Image.Image],
        prompt_phase1: Optional[str] = None,
        prompt_phase2: Optional[str] = None,
        num_inference_steps: int | None = None,
        guidance_scale: float | None = None,
        seed: int = -1,
        width: Optional[int] = None,
        height: Optional[int] = None,
        padding_ratio: float | None = None,
    ) -> Tuple[Image.Image, Image.Image, Dict]:
        """
        Two-phase image enhancement process.
        
        Phase 1: Enhance the generated image to fix artifacts and improve realism.
        Phase 2: Use the Phase 1 output with reference image for identity matching.
        
        Args:
            generated_image: The image generated by view_generator.py
            reference_image: The original reference image for identity
            prompt_phase1: Custom prompt for Phase 1 (uses FIRST_PHASE_PROMPT if None)
            prompt_phase2: Custom prompt for Phase 2 (uses SECOND_PHASE_PROMPT if None)
            num_inference_steps: Number of inference steps (default: 4 for distilled)
            guidance_scale: Guidance scale (default: 1.0 for distilled)
            seed: Random seed (-1 for random)
            width: Output width (auto-computed if None)
            height: Output height (auto-computed if None)
            padding_ratio: Padding ratio for foreground centering (default: 0.1 = 10%)
            
        Returns:
            Tuple of (phase1_image, phase2_image, crop_info)
        """
        if self.pipe is None:
            self.load_pipeline()

        ev = get_config().enhance_view
        if num_inference_steps is None:
            num_inference_steps = ev.num_inference_steps
        if guidance_scale is None:
            guidance_scale = ev.guidance_scale
        if padding_ratio is None:
            padding_ratio = ev.padding_ratio
        
        # Load images if paths
        if isinstance(generated_image, (str, Path)):
            generated_image = Image.open(generated_image).convert("RGB")
        if isinstance(reference_image, (str, Path)):
            reference_image = Image.open(reference_image).convert("RGB")
        
        # Store TRUE original size before any processing
        true_original_size = generated_image.size
        
        # Compute dimensions if not provided
        if width is None or height is None:
            width, height = compute_output_dimensions(generated_image)
        
        target_size = (width, height)
        
        # Preprocessing: Apply matting in ORIGINAL coordinate space first (no resize)
        # This ensures crop_info is computed in original space for correct inverse transformation
        foreground_original, original_mask = self.bg_remover.remove_background(generated_image)
        generated_matted_original = apply_white_background(foreground_original, original_mask)
        
        # Crop and center foreground with padding - computed in ORIGINAL coordinate space
        # The crop_info will have original_size = true_original_size
        centered_image, crop_info = crop_and_center_foreground(
            generated_matted_original,
            original_mask,
            padding_ratio=padding_ratio,
            target_size=target_size,
        )
        
        # Apply matting to the centered image (already at target_size)
        centered_image = preprocess_image_with_matting(
            centered_image,
            self.bg_remover,
            target_size=target_size,
        )
        
        # Phase 1: Single image enhancement with centered image
        phase1_result = self.enhance_image_phase1(
            generated_image=centered_image,
            prompt=prompt_phase1,
            num_inference_steps=num_inference_steps,
            guidance_scale=guidance_scale,
            seed=seed,
            width=width,
            height=height,
        )

        phase1_result_copy = phase1_result.convert("RGB")
        # Phase 2: Identity-guided enhancement
        phase2_result = self.enhance_image_phase2(
            phase1_image=phase1_result_copy,
            reference_image=reference_image,
            prompt=prompt_phase2,
            num_inference_steps=num_inference_steps,
            guidance_scale=guidance_scale,
            seed=seed,
            width=width,
            height=height,
        )
        
        return phase1_result, phase2_result, crop_info
    
    def unload(self):
        """Unload pipeline and free GPU memory."""
        if self.pipe is not None:
            del self.pipe
            self.pipe = None
        if self.bg_remover is not None:
            del self.bg_remover
            self.bg_remover = None
        flush_gpu_memory()


