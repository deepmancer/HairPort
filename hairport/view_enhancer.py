"""
View Enhancer using FLUX.2 Klein 9B (Distilled)

This module enhances images generated by view_generator.py using the FLUX.2 Klein 9B
distilled model with only 4 inference steps.

For each sample, the enhancement process:
1. Loads the generated image from view_generator.py
2. Loads the corresponding reference image
3. Applies matting to both images (converts backgrounds to white)
4. Uses FLUX.2 Klein to enhance/refine the image
5. Saves the enhanced output as target_image_fk.png
"""

