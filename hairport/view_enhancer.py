def process_view_aligned_folder(
    folder_path: Path,
    data_dir: Path,
    bald_version: str = "w_seg",
    seed: int = -1,
    enhancer: Optional[ViewEnhancer] = None,
) -> bool:
    """
    Process a single view-aligned folder to enhance the generated image.
    
    Args:
        folder_path: Path to the view-aligned folder
        data_dir: Root data directory
        bald_version: Bald version mode ("w_seg", "wo_seg")
        seed: Random seed for enhancement
        enhancer: ViewEnhancer instance (creates new one if None)
        
    Returns:
        True if successful, False otherwise
    """
    # Define paths
    input_image_path = folder_path / "alignment" / "target_image.png"
    phase1_output_path = folder_path / "alignment" / "target_image_phase_1.png"
    final_output_path = folder_path / "alignment" / "target_image_phase_2.png"
    folder_name = folder_path.name
    
    # Check if input exists
    if not input_image_path.exists():
        return False
    
    # Skip if already processed (check final output)
    # if phase1_output_path.exists():
    #     return False
    
    # Parse folder name to extract target_id
    # Format: {target_id}_to_{source_id}
    parts = folder_name.split("_to_")
    if len(parts) != 2:
        return False
    
    target_id, source_id = parts
    
    # Determine reference image path based on dataset
    dataset_name = Path(data_dir).name
    if dataset_name == "celeba_reduced":
        image_folder = "image_outpainted"
    else:
        image_folder = "hair_aligned_image"
    
    reference_image_path = data_dir / image_folder / f"{target_id}.png"
    
    if not reference_image_path.exists():
        return False
    
    print(f"Input: {input_image_path}")
    print(f"Reference: {reference_image_path}")
    
    try:
        # Create enhancer if not provided
        should_unload = False
        if enhancer is None:
            enhancer = ViewEnhancer(load_pipeline=True)
            should_unload = True
        
        # Run two-phase enhancement
        phase1_image, phase2_image, crop_info = enhancer.enhance_image(
            generated_image=str(input_image_path),
            reference_image=str(reference_image_path),
            seed=seed,
        )
        
        # Save raw Phase 1 output (before inverse transformation)
        raw_output_path = folder_path / "alignment" / "target_image_phase_1_raw.png"
        raw_output_path.parent.mkdir(parents=True, exist_ok=True)
        if phase1_image.mode != "RGB":
            phase1_image_rgb = phase1_image.convert("RGB")
        else:
            phase1_image_rgb = phase1_image
        phase1_image_rgb.save(raw_output_path)
        print(f"Phase 1 raw output: {raw_output_path}")
        
        # Apply inverse transformation to restore original spatial dimensions
        phase1_uncropped = uncrop_to_original(phase1_image, crop_info)
        phase2_uncropped = uncrop_to_original(phase2_image, crop_info)
        
        # Save outputs - ensure RGB mode for white background (no transparency)
        phase1_output_path.parent.mkdir(parents=True, exist_ok=True)
        if phase1_uncropped.mode != "RGB":
            phase1_uncropped = phase1_uncropped.convert("RGB")
        phase1_uncropped.save(phase1_output_path)
        print(f"Phase 1 output: {phase1_output_path}")
        
        if phase2_uncropped.mode != "RGB":
            phase2_uncropped = phase2_uncropped.convert("RGB")
        phase2_uncropped.save(final_output_path)
        print(f"Phase 2 output: {final_output_path}")
        
        if should_unload:
            enhancer.unload()
        
        return True
        
    except Exception as e:
        print(f"Error: {folder_name}: {e}")
        return False


def enhance_view_ig2mv(
    generated_image: Union[str, Path, Image.Image],
    reference_image: Union[str, Path, Image.Image],
    prompt_phase1: Optional[str] = None,
    prompt_phase2: Optional[str] = None,
    seed: int = -1,
    num_inference_steps: int | None = None,
    guidance_scale: float | None = None,
    padding_ratio: float | None = None,
    enhancer: Optional[ViewEnhancer] = None,
) -> Tuple[Image.Image, Image.Image, Dict]:
    """
    Convenience function for quick two-phase enhancement.
    
    Args:
        generated_image: The image generated by view_generator.py
        reference_image: The original reference image
        prompt_phase1: Custom prompt for Phase 1 (uses default if None)
        prompt_phase2: Custom prompt for Phase 2 (uses default if None)
        seed: Random seed
        num_inference_steps: Number of inference steps
        guidance_scale: Guidance scale
        padding_ratio: Padding ratio for foreground centering (default: 0.1 = 10%)
        enhancer: ViewEnhancer instance (creates new one if None)
        
    Returns:
        Tuple of (phase1_image, phase2_image, crop_info)
    """
    should_unload = False
    if enhancer is None:
        enhancer = ViewEnhancer(load_pipeline=True)
        should_unload = True
    
    try:
        result = enhancer.enhance_image(
            generated_image=generated_image,
            reference_image=reference_image,
            prompt_phase1=prompt_phase1,
            prompt_phase2=prompt_phase2,
            seed=seed,
            num_inference_steps=num_inference_steps,
            guidance_scale=guidance_scale,
            padding_ratio=padding_ratio,
        )
        return result
    finally:
        if should_unload:
            enhancer.unload()


